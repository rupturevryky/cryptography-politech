# Словарь для шифрования и расшифрования
lookup_table = {
    'А': 'Я', 'Б': 'Ю', 'В': 'Э', 'Г': 'Ь', 'Д': 'Ы', 'Е': 'Ъ',
    'Ж': 'Щ', 'З': 'Ш', 'И': 'Ч', 'Й': 'Ц', 'К': 'Х', 'Л': 'Ф',
    'М': 'У', 'Н': 'Т', 'О': 'С', 'П': 'Р', 'Р': 'П', 'С': 'О',
    'Т': 'Н', 'У': 'М', 'Ф': 'Л', 'Х': 'К', 'Ц': 'Й', 'Ч': 'И',
    'Ш': 'З', 'Щ': 'Ж', 'Ъ': 'Е', 'Ы': 'Д', 'Ь': 'Г', 'Э': 'В',
    'Ю': 'Б', 'Я': 'А'
}

def atbash_cipher(text):
    """Функция для шифрования/расшифрования текста"""
    result = ""
    for char in text.upper():
        if char in lookup_table:
            result += lookup_table[char]
        else:
            result += char
    return result

def main():
    """Основная функция программы"""
    print("Программа шифрования/расшифрования Атбаш")
    print("----------------------------------------")
    
    while True:
        print("\nВыберите действие:")
        print("1. Зашифровать текст")
        print("2. Расшифровать текст")
        print("3. Выход")
        
        choice = input("\nВведите номер действия (1-3): ")
        
        if choice == '3':
            print("До свидания!")
            break
            
        if choice in ['1', '2']:
            text = input("\nВведите текст для обработки: ")
            
            # Проверка на корректность ввода
            if not any(char.upper() in lookup_table for char in text):
                print("Ошибка: текст должен содержать хотя бы одну русскую букву!")
                continue
                
            result = atbash_cipher(text)
            print(f"\nРезультат: {result}")
        else:
            print("\nОшибка: выберите действие от 1 до 3!")

if __name__ == "__main__":
    main()